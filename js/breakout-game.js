function setCard() {
    resetCard();

    var card = null;

    if (level === 1) {
        card = qsLevel1[indexCard];
    } else if (level === 2) {
        card = qsLevel2[indexCard];
        var index = indexCard + 1;
        cardTitle.html(`CÂU HỎI ${index}/5`);

    } else if (level === 3) {
        card = qsLevel3[indexCard];
        cardTitle.html(`TRẢ LỜI ĐÚNG ${indexLevel3} CÂU`);
    }

    qsTitle.html(card.title);

    createAnswers(card.answers);

}

function checkCard(index) {

    var card = null;

    if (level === 1) {
        card = qsLevel1[indexCard];
    } else if (level === 2) {
        card = qsLevel2[indexCard];
    } else if (level === 3) {
        card = qsLevel3[indexCard];
    }

    return card && card.answers[index].result;

}

function initGame() {

    if (level === 1) {
        roundTitle.html(titleRound1);
        cardTitle.html(`CÂU HỎI 1/10`);

    } else if (level === 2) {
        roundTitle.html(titleRound2);
        cardTitle.html(`CÂU HỎI 1/5`);
    } else if (level === 3) {
        roundTitle.html(titleRound3);
        cardTitle.html(`TRẢ LỜI ĐÚNG 0 CÂU`);
    }

    setCard();

}



